def main():
    seen = set()
    rows = []
    for (year, topic, text, a, b, c, d, ans, expl) in Q:
        assert 2016 <= year <= 2025, f"year out of range: {year}"
        assert ans in ("A", "B", "C", "D"), f"bad answer: {ans}"
        opts = {"A": a, "B": b, "C": c, "D": d}
        assert all(v and v.strip() for v in opts.values()), f"empty option in: {text}"
        key = (year, text.strip().lower())
        if key in seen:
            continue
        seen.add(key)
        rows.append({
            "subject": "government",
            "exam": "jamb",
            "year": year,
            "topic": topic,
            "source": "past",
            "difficulty": 2,
            "text": text,
            "options": opts,
            "answer": ans,
            "explanation": expl,
        })
    with open(OUT, "w", encoding="utf-8") as f:
        for r in rows:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")

    years = sorted({r["year"] for r in rows})
    by_year = {}
    for r in rows:
        by_year[r["year"]] = by_year.get(r["year"], 0) + 1
    print(f"WROTE {len(rows)} rows to {OUT}")
    print(f"YEARS {years}")
    print(f"PER_YEAR {by_year}")

if __name__ == "__main__":
    main()
